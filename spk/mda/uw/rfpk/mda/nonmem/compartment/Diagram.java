    private int getModelType(String text)
    {
        if(text.length() == 0) return 0;
        int type = 1;
        if(text.indexOf("\n") == -1)
        {
            String[] sides = text.split("=");
            if(sides == null || sides.length != 2) return 0;
            if(!Utility.isFloatNumber(sides[1]))
            {
                if(sides[1].indexOf("THETA(") != -1)
                    type = 2;
                else type = 3;
            }
        }
        else
            type = 2;   
        return type;
    }
 
